const printNumberRange = (start, finish) => {
    const array = []
    for (let i = start; i <= finish; i++) {
        array.push(i)
    }
    
    return array.join(', ')
}


// first argument should number start,
//  second argument should number stop
console.log(printNumberRange(4,8))
